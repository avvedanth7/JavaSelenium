package nucot.javasessions.com;

public class FloatingPointDemo {

	public static void main(String[] args) {
	
		
		//5.float
		//size:4bytes-->8*4=32bits
		//range:it can take upto 7 digits
		
		//one way of declaring the float value
		float f=10.00f;
		
		System.out.println(f);
		
		
		//second way of declaring the float value
		
		float f1=(float)1000000.08;
		
		
		System.out.println(f1);
		
		
		
		//6.double
		//size:8bytes-->8*8=64bits
		//range:it can take upto 15 digits
	
		double d=111.999;
		System.out.println(d);
		
		
		double dd=113455666761.99944440000;
		System.out.println(dd);
		
		
		//7.char
		//size:2bytes-->8*2=16bits
		//a-z A-Z 0-9 special symbols
		
		char c='a';
	
		char cc='Z';
	
		char ccc='$';
	
		char cccc='9';
		System.out.println(c+"--->"+cc+"-->"+ccc+"--->"+cccc);
		
		
		
		
		//8.boolean
		//size:1bit-->true /false
		
				
			boolean gg=true;
			System.out.println(gg);
		
		
			boolean ggg=false;
			System.out.println(ggg);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
